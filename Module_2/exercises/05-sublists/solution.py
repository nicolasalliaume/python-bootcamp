#############################################
#
# It's really easy to get a portion of a list.
# You can use the_list[<from>:<to>], where <from>
# is one index and <to> is another index.
#
# Try to print from index 2 to 5 of the given
# list.
#
# Tip: using print() with a list as a parameter will
# do fine.
#
#############################################

the_list = ['some', 'nonsense', 'elements', 'of', 'a', 'list', 'are', 'written', 'in', 'here']

print ( the_list[2:6] ) # notice a 6? last index is not included, so you need to +1 the <to> index!